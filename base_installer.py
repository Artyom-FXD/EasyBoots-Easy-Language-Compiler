    def install(self, version=None):
        if not self._show_license():
            return 1

        if sys.platform == 'win32':
            return self._install_windows(version)
        elif sys.platform.startswith('linux'):
            return self._install_linux(version)
        elif sys.platform == 'darwin':
            return self._install_macos(version)
        else:
            print(f"Unsupported platform: {sys.platform}")
            return 1

    def _install_windows(self, version):
        # Используем предсобранные MinGW-w64 от winlibs.com
        # URL для версии по умолчанию (можно расширить выбор версий)
        url = "https://github.com/brechtsanders/winlibs_mingw/releases/download/13.2.0-16.0.6-11.0.1-msvcrt-r1/winlibs-x86_64-posix-seh-gcc-13.2.0-mingw-w64msvcrt-11.0.1-r1.7z"
        print(f"Downloading GCC from {url}...")

        self.tools_dir.mkdir(exist_ok=True)
        archive_path = self.tools_dir / "gcc.7z"

        try:
            urllib.request.urlretrieve(url, archive_path)
        except Exception as e:
            print(f"Download failed: {e}")
            return 1

        # Проверяем наличие 7z
        seven_zip = shutil.which('7z')
        if not seven_zip:
            print("7-Zip not found in PATH. Please install 7-Zip or add it to PATH.")
            return 1

        # Удаляем старую установку, если есть
        if self.gcc_dir.exists():
            shutil.rmtree(self.gcc_dir)
        self.gcc_dir.mkdir(parents=True)

        print("Extracting archive...")
        subprocess.run([seven_zip, 'x', str(archive_path), f'-o{self.gcc_dir}'], check=True)
        archive_path.unlink()  # удаляем архив

        # Ожидаемая структура: внутри архива папка mingw64, перемещаем всё на уровень выше
        extracted_root = self.gcc_dir / 'mingw64'
        if extracted_root.exists():
            for item in extracted_root.iterdir():
                shutil.move(str(item), str(self.gcc_dir))
            extracted_root.rmdir()

        print(f"GCC installed successfully to {self.gcc_dir}")
        return 0

    def _install_linux(self, version):
        print("On Linux it is recommended to install GCC using your package manager.")
        print("Example: sudo apt install gcc    (Debian/Ubuntu)")
        print("         sudo dnf install gcc    (Fedora)")
        print("         sudo pacman -S gcc      (Arch)")
        resp = input("Do you want to proceed with manual download? (not recommended) [y/N]: ")
        if resp.lower() != 'y':
            return 0
        # Для простоты можно также загружать с официального сайта GCC, но это сложно.
        # Вместо этого просто проверяем наличие gcc в системе.
        if shutil.which('gcc'):
            print("GCC is already available in PATH.")
        else:
            print("GCC not found. Please install it via your package manager.")
        return 0

    def _install_macos(self, version):
        print("On macOS, please install GCC via Homebrew: brew install gcc")
        return 0

    def remove(self):
        if self.gcc_dir.exists():
            shutil.rmtree(self.gcc_dir)
            print("GCC removed from tools/")
        else:
            print("No GCC installation found in tools/")
        return 0

    def list_versions(self):
        print("Available GCC versions for Windows (winlibs):")
        print(" - 13.2.0 (default)")
        # Можно добавить парсинг страницы релизов GitHub
        return 0

    def set_custom_path(self, path_str):
        path = Path(path_str).resolve()
        if not path.exists() or not path.is_file():
            print(f"Error: '{path}' does not exist or is not a file.")
            return 1

        config = {}
        if self.config_file.exists():
            with open(self.config_file, 'r') as f:
                config = json.load(f)
        config['compiler_path'] = str(path)
        with open(self.config_file, 'w') as f:
            json.dump(config, f, indent=4)
        print(f"Custom compiler path set to {path}")
        return 0